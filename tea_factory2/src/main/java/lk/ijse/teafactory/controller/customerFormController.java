    @FXML
    void btnSaveOnAction(ActionEvent event) {
        int customer_id= Integer.parseInt(txtCustomerId.getText());
        String customer_name = txtCustomerName.getText();
        String customer_address= txtAddress.getText();
        String status= txtStatus.getText();



        String sql = "INSERT INTO customer VALUES(?, ?, ?,?)";

        try {
            Connection connection = lk.ijse.teafactory.db.DbConnection.getInstance().getConnection();
            PreparedStatement pstm = connection.prepareStatement(sql);

            ((PreparedStatement) pstm).setObject(1, customer_id);
            pstm.setObject(2, customer_name);
            pstm.setObject(3, customer_address);
            pstm.setObject(4, status);



            boolean isSaved = pstm.executeUpdate() > 0;
            if(isSaved) {
                new Alert(Alert.AlertType.CONFIRMATION, " customer  saved!").show();
                clearFields();
            }
        } catch (SQLException e) {
            new Alert(Alert.AlertType.ERROR, e.getMessage()).show();
        }
    }

    private void clearFields() {
        txtCustomerId.setText("");
        txtCustomerName.setText("");
        txtAddress.setText("");
        txtStatus.setText("");


    }


    @FXML
    void btnUpdateOnAction(ActionEvent event) throws SQLException {
        int customer_id= Integer.parseInt(txtCustomerId.getText());
        String customer_name = txtCustomerName.getText();
        String customer_address= txtAddress.getText();
        String status= txtStatus.getText();

        boolean isUpdated = CustomerRepo.update(new Customer(customer_id,customer_name,customer_address,status));

        if (isUpdated) {
            new Alert(Alert.AlertType.CONFIRMATION, "Updated!").show();
            clearFields();
        }
        loadAllCustomers();
    }

    private void loadAllCustomers() {
        ObservableList<CustomerTm> customerTMS = FXCollections.<CustomerTm>observableArrayList();

        try {
            List<Customer> customerList = CustomerRepo.getAll();
            for (Customer customer : customerList) {
                customerTMS.add(new CustomerTm(
                        customer.getCustomer_id(),
                        customer.getCustomer_address(),
                        customer.getCustomer_name(),
                        customer.getCustomer_status()

                ));
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
        tblCustomer.setItems(customerTMS);
    }
